const {apis} = require('../config');
const translate = require('google-translate-api');

exports.run = (client, msg, args) => {
    return msg.channel.fetchMessages({limit: 1,before: msg.id}).then(async (messages) => {
        if(args[0]) {
            let query = client.clean(messages.array()[0].cleanContent);
            if(!langs[args[0]]) return msg.edit(`That is not a supported language`);
            translate(query, {to: args[0]}).then(res => {
                return msg.edit(`Translated to ${langs[args[0]]} \`\`\`${res.text}\`\`\``)
    
            }).catch(err => {
                console.error(`[Translate] ${err}`)
                return msg.edit('ðŸ‡½ Translation failed, check console for details');
            })
            return;
        }

        let query = client.clean(messages.array()[0].cleanContent);
        translate(query, {to: 'en'}).then(res => {
            return msg.edit(`Translated from ${langs[res.from.language.iso]} \`\`\`${res.text}\`\`\``)

        }).catch(err => {
            console.error(`[Translate] ${err}`)
            return msg.edit('ðŸ‡½ Translation failed, check console for details');
        })
    });
}

exports.conf = {
  enabled: true,
  guildOnly: false,
  aliases: [],
  permLevel: 0
};

exports.help = {
  name: 'translate',
  description: 'translates last message to english',
  usage: 'translate <timezone>'
};


